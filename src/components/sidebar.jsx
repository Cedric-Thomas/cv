function RenderSkill(skill, idx) {
    if (typeof skill === 'object') {
        return (
            <div key={idx} className="sublist">
                <span className="subsubsection">{Object.keys(skill)[0]} : </span>
                {Object.values(skill)[0].map((sub, idx) => (
                    <span key={idx} className="details italic">{sub} </span>
                ))}
            </div>
        )
    } else {
        return (
            <span key={idx}>{skill} </span>
        )
    }
}

